export function normalizeKey(value) {
  return (value || '')
    .toString()
    .trim()
    .toLowerCase()
    .replace(/[.,]/g, '')
    .replace(/district|urban|rural|city|municipal|metropolitan/g, '')
    .replace(/[\s-]+/g, '_')
    .replace(/_+/g, '_')
    .replace(/^_+|_+$/g, '');
}

export function getIndiaFoodSuggestions({ state, city }) {
  const stateKey = normalizeKey(state);
  if (!stateKey || !indiaRegionFoods[stateKey]) return null;

  const entry = indiaRegionFoods[stateKey];
  const cityKey = normalizeKey(city);
  let specialties = [];

  if (cityKey && entry.cities?.[cityKey]) {
    specialties = entry.cities[cityKey];
  } else if (cityKey) {
    const cityKeys = Object.keys(entry.cities || {});
    const match = cityKeys.find((key) => cityKey.includes(key) || key.includes(cityKey));
    if (match) specialties = entry.cities[match];
  }

  if (!specialties.length && Array.isArray(entry.specialties)) {
    specialties = entry.specialties;
  }

  return {
    cuisines: entry.cuisines || [],
    specialties
  };
}
